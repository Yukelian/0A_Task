	g_spiTxBuf[g_spiLen++] = ((_uiReadAddr & 0xFF00) >> 8);	   /* ????????????§Þ?8bit */
	g_spiTxBuf[g_spiLen++] = (_uiReadAddr & 0xFF);			   /* ?????????????8bit */
